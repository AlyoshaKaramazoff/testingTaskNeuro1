class hangup_logic():

    def __init_(self):
        pass

    def hangup_wrong_time(self):
        print("\nИзвините пожалуйста за беспокойство. Всего вам доброго")
        return None

    def hangup_null(self):
        print("Вас все равно не слышно, будет лучше если я перезвоню. Всего вам доброго")
        return None

    def hangup_positive(self):
        print("\nОтлично!  Большое спасибо за уделенное время! Всего вам доброго!")
        return None

    def hangup_negative(self):
        print("\nЯ вас понял. В любом случае большое спасибо за уделенное время! Всего вам доброго. ")
        return None

    def forward(self):
        print("\nЧтобы разобраться в вашем вопросе, я переключу звонок на моих коллег. Пожалуйста оставайтесь на линии.")
        return None